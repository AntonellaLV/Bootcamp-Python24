# Crear una lista con 5 números
numeros = [4, 7, 10, 23, 1]

# Inicializar la variable para almacenar la suma
suma_total = 0

# Usar un bucle for para sumar los elementos de la lista
for numero in numeros:
    suma_total += numero  # Sumar cada número al total

# Imprimir el resultado de la suma
print(f"La suma de los elementos de la lista es: {suma_total}")